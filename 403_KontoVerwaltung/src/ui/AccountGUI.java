	private void refreshReferences(List<AccountUser> users) {
		for (AccountUser u : users) {
			u.setSharedAccount(sharedAccount);
			u.setOutput(transfer_output);
		}
	}

	private void startWorkers(List<AccountUser> users) throws InterruptedException {
		for (AccountUser u : users) {
			Thread t = new Thread(u, u.getName());
			t.setPriority(Thread.NORM_PRIORITY + 1);
			t.start();
		}
	}

